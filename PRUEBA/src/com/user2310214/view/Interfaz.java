    void ObtenerLista() {
        
         GastoRepository repository = new GastoRepository(url, user, password);
         
         List<Gastos> List = repository.findAll();
         
         DefaultTableModel dtm = new DefaultTableModel();
         String titulos[]={"ID","Titulo","Autor","Genero","AÃ±o"};
         dtm.setColumnIdentifiers(titulos);
         jtabla.setModel(dtm);
         
         // Borramos si tiene algo la tabla
          int cantFilas = dtm.getRowCount();
        if (cantFilas>0){
            for(int i=0;i<cantFilas;i++){
                dtm.removeRow(0);
            }
        }
        
        String datos[] = new String[10];
         
         for (Gastos gastos : List) {
                 datos[0] = String.valueOf(gastos.getId());
                 datos[1] = gastos.getDescripcion();
                 datos[2] = gastos.getCategoria();
                 datos[3] = gastos.getFecha();
                 datos[4] = String.valueOf(gastos.getFecha());
                 dtm.addRow(datos);
         }
    }

