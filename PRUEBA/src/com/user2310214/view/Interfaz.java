    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
                GastoRepository repository = new GastoRepository(url, user, password);
                Gastos book1 = new Gastos.GastorBuilder()
                .setDescripcion(txtdescripcion.getText())
                .setCategoria(txtcategoria.getText())
                .setFecha(txtfecha.getText())
                .setMonto(Integer.parseInt(txtmonto.getText()))
                .build();
                
        repository.save(book1);
        JOptionPane.showMessageDialog(rootPane, "Libro Creado");
        ObtenerLista();
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane, "Error al crear el libro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jtablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtablaMouseClicked
          int fila = jtabla.getSelectedRow();
        String id =jtabla.getValueAt(fila, 0).toString();
        
        try {
            GastoRepository repository = new GastoRepository(url, user, password);
             
            repository.delete(Integer.parseInt(id));
            JOptionPane.showMessageDialog(rootPane, "Libro Eliminado");
            ObtenerLista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al eliminar el libro");
        }
        
       
    }//GEN-LAST:event_jtablaMouseClicked

